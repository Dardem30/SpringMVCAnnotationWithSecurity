package by.model;

import javax.persistence.*;
import java.util.Set;

/**
 * Created by УВД on 11.08.2017.
 */
@Entity
@Table(name = "user", schema = "mysecurity", catalog = "")
public class UserEntity {
    private int id;
    private String username;
    private String password;
    private Set<RoleEntity> roleEntitySet;

    public UserEntity() {
    }

    public UserEntity(int id, String username, String password, Set<RoleEntity> roleEntitySet) {
        this.id = id;
        this.username = username;
        this.password = password;
        this.roleEntitySet = roleEntitySet;
    }

    public UserEntity(int id) {
        this.id = id;
    }

    @Id
    @Column(name = "id")
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Basic
    @Column(name = "username")
    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    @Basic
    @Column(name = "password")
    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    @ManyToMany(mappedBy = "userEntities",fetch = FetchType.EAGER)
    public Set<RoleEntity> getRoleEntitySet() {
        return roleEntitySet;
    }

    public void setRoleEntitySet(Set<RoleEntity> roleEntitySet) {
        this.roleEntitySet = roleEntitySet;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        UserEntity that = (UserEntity) o;

        if (id != that.id) return false;
        if (username != null ? !username.equals(that.username) : that.username != null) return false;
        if (password != null ? !password.equals(that.password) : that.password != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = id;
        result = 31 * result + (username != null ? username.hashCode() : 0);
        result = 31 * result + (password != null ? password.hashCode() : 0);
        return result;
    }
}
